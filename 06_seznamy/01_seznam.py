"""Vytváření seznamů -> hranaté závorky {Alt + F a G}"""

cisla = [1, 1, 2, 3, 4, 5 , 8, 13]
print(cisla)

"""Seznam je hodnota, která může obsahovat spoustu dalších hodnot.
Tam jako řetězec obsahuje sekvenci znaků, seznam obsahuje sekvenci čehokoliv (např. čísel).
A tak jako můžeme pomocí cyklu for procházet řetězec po znacích, seznam můžeme procházet po jednotlivých prvcích."""

for cislo in cisla:
    print(cislo)