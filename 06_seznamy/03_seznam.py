"""Druhá nejdůležitější operace je vybírání jednotlivých prvků.
    To funguje jako u řetězců: do hranatých závorek [] se dá číslo prvku.
    Čísluje se jako u řetězců, od 0; záporná čísla označují prvky od konce!"""

cisla = [1, 1, 6, 3, 5, 8, 13]
print(cisla[2])

"""Hranatými závorkami můžeme získávat podseznamy"""

print(cisla[2:-3])